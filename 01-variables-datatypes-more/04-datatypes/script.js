// Number
const age = '30';
const temp = '96.6';

console.log(age, typeof age);
console.log(temp, typeof temp);

// Boolean
const hasKids = true;

console.log(hasKids, typeof hasKids);

// Null
const apartment_num = null;

console.log(apartment_num, typeof apartment_num);

// Undefined
let score;
let scores = undefined;

console.log(score, typeof score);
console.log(scores, typeof scores);


